import { spawn } from "node:child_process";
import { mkdir, readFile, writeFile } from "node:fs/promises";
import { resolve } from "node:path";

const rootDir = process.cwd();
const outputDir = resolve(rootDir, ".tmp", "ci-bird-smoke");
const targetConfigPath = resolve(outputDir, "bird.conf");
const smokeConfigText = [
  "router id 192.0.2.1;",
  "",
  "protocol device dev1 {}",
  "",
  "protocol direct direct1 {",
  "  ipv4;",
  "}",
  "",
  "protocol kernel k4 {",
  "  ipv4 {",
  "    export all;",
  "  };",
  "}",
  "",
  "protocol static st4 {",
  "  ipv4;",
  "  route 198.51.100.0/24 blackhole;",
  "}",
  "",
].join("\n");

const run = async (command, args, label) => {
  await new Promise((resolvePromise, rejectPromise) => {
    const child = spawn(command, args, {
      cwd: rootDir,
      stdio: "inherit",
      shell: false,
    });

    child.on("error", rejectPromise);
    child.on("exit", (code) => {
      if (code === 0) {
        resolvePromise();
        return;
      }
      rejectPromise(new Error(`${label} failed with exit code ${String(code)}`));
    });
  });
};

await mkdir(outputDir, { recursive: true });
await writeFile(targetConfigPath, smokeConfigText, "utf8");

await run(
  "node",
  [
    "packages/@birdcc/cli/dist/cli.js",
    "lint",
    targetConfigPath,
    "--format",
    "json",
  ],
  "birdcc lint",
);

await run(
  "node",
  [
    "packages/@birdcc/cli/dist/cli.js",
    "fmt",
    targetConfigPath,
    "--write",
    "--engine",
    "builtin",
  ],
  "birdcc fmt",
);

const finalConfig = await readFile(targetConfigPath, "utf8");
if (!finalConfig.includes("protocol")) {
  throw new Error("formatted config is unexpectedly empty or invalid");
}

await writeFile(resolve(outputDir, "README.txt"), "Generated by CI smoke script.\n", "utf8");
console.log(`CI_BIRD_SMOKE_CONFIG=${targetConfigPath}`);
